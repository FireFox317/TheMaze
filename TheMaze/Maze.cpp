Maze::~Maze()
{
}

void Maze::printMaze() {
	for (auto row : data) {
		for (int element : row) {
			if (element == 0) {
				std::cout << "#";
			}
			else if (element == 1) {
				std::cout << ".";
			}
			else if (element == 2) {
				std::cout << "x";
			}
			std::cout << " ";
		}
		std::cout << std::endl;
	}
}
